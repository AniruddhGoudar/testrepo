    private void btnSeeMoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeeMoreActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex = tblCarBuy.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)tblCarBuy.getModel();
        
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select the car to see more details");
        }

      String carId = (String) model.getValueAt(selectedRowIndex, 0);
        
        System.out.println("carId "+carId);
        
            CustomersSeller custSeller = custDirectory.getCustomersSeller().stream()
                    .filter(it -> it.getUniqueId().equals(carId))
                    .findFirst() 
                    .orElse(null);
            
            ImageIcon logoImage = new ImageIcon(custSeller.getLogoImage().getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH));
            
        txtModel.setText(custSeller.getCarModel());
        txtManufactureYear.setText(custSeller.getManufacteryear());
        txtMilesDriven.setText(custSeller.getKmsDriven());
        cmbFuelType.setSelectedItem(custSeller.getFuelType());
        cmbTransmission.setSelectedItem(custSeller.getTransmission());
        txtAccidents.setText(custSeller.getNoOfAccidents());
        txtCarCompany.setText(custSeller.getCarCompany());
        txtSellPrice.setText(custSeller.getSellingPrice());
        imgLogo.setIcon(logoImage);
    }//GEN-LAST:event_btnSeeMoreActionPerformed


