    private void loadCarTable() {
        DefaultTableModel model = (DefaultTableModel)tblCarBuy.getModel();
        model.setRowCount(0);
        
        Dimension desiredImageSize = new Dimension(2, 2);
       
        tblCarBuy.getColumnModel().getColumn(4).setCellRenderer(new ImageTableCellRenderer(desiredImageSize));

        for (CustomersSeller c : custDirectory.getCustomersSeller()) {
            Object row[] = new Object[5];
            if(c.getType() == "Sold"){
            row[0] = c.getUniqueId();
            row[1] = c.getCarCompany();
            row[2] = c.getCarModel();
            row[3] = c.getManufacteryear();
            row[4] = c.getLogoImage();
            model.addRow(row);
            }
           
        }                 
    }
      
       
